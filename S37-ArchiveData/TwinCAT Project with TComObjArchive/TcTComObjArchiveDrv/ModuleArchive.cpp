///////////////////////////////////////////////////////////////////////////////
// State transition from SAFEOP to PREOP
HRESULT CModuleArchive::SetObjStateSP()
{
	HRESULT hr = S_OK;
	m_Trace.Log(tlVerbose, FENTERA);

	// Store current state to archive
	ITComObjArchiveServerPtr spAS = m_spSrv;
	if (spAS == NULL)
	{
		m_Trace.Log(tlError, FNAMEA "TCom Object Archive Server not available"); 
	}
	else
	{
		// Create object archive for write, the archive resides in the TwinCAT boot directory in 
		// in subdirectory TcCom

		ITComArchiveOpPtr spAr; 
		if (FAILED(hr = spAS->CreateObjArchive(THIS_ITCOMOBJECT, FALSE, IID_ITComArchiveOp, (PPVOID)&spAr)))
		{
			m_Trace.Log(tlError, FNAMEA "CreateObjArchive failed (hr=0x%08x)", hr); 
		}
		else
		{
			hr = StoreToArchive(spAr); 
			if (FAILED(hr))
			{
				m_Trace.Log(tlError, FNAMEA "StoreToArchive failed (hr=0x%08x)", hr); 
			}
		}
	}

	m_Trace.Log(tlVerbose, FLEAVEA "hr=0x%08x", hr);
	return hr;
}

