///////////////////////////////////////////////////////////////////////////////
// State transition from PREOP to SAFEOP
//
// Initialize input parameters 
// Allocate memory
HRESULT CModuleArchive::SetObjStatePS(PTComInitDataHdr pInitData)
{
	m_Trace.Log(tlVerbose, FENTERA);
	HRESULT hr = S_OK;
	IMPLEMENT_ITCOMOBJECT_EVALUATE_INITDATA(pInitData);

	// Open archive and restore state
	ITComObjArchiveServerPtr spAS = m_spSrv;
	if (spAS == NULL)
	{
		m_Trace.Log(tlError, FNAMEA "TCom Object Archive Server not available"); 
	}
	else
	{
		// Create object archive for read, the archive resides in the TwinCAT boot directory in 
		// in subdirectory TcCom

		ITComArchiveOpPtr spAr; 
		if (FAILED(hr = spAS->CreateObjArchive(THIS_ITCOMOBJECT, TRUE, IID_ITComArchiveOp, (PPVOID)&spAr)))
		{
			if (hr == ADS_E_NOTFOUND)
			{
				m_Trace.Log(tlWarning, FNAMEA "CreateObjArchive returned file not found, using defaults (hr=0x%08x)", hr); 
				hr = S_OK;
			}
			else
			{
				// ADS_E_INCOMPATIBLE = file content incompatible
				// or other error codes
				m_Trace.Log(tlWarning, FNAMEA "CreateObjArchive failed (hr=0x%08x)", hr); 
			}
		}
		else
		{
			if (hr == S_OLDBOOTDATA)
			{
				m_Trace.Log(tlWarning, FNAMEA "CreateObjArchive found only old boot data"); 
			}
			hr = RestoreFromArchive(spAr); 
			if (FAILED(hr))
			{
				m_Trace.Log(tlError, FNAMEA "RestoreFromArchive failed (hr=0x%08x)", hr); 
			}
		}
	}

	m_Trace.Log(tlVerbose, FLEAVEA "hr=0x%08x", hr);
	return hr;
}

